var color = "magenta";

switch(color) {
    case "red" : console.log("color is red"); break;
    case "blue" : console.log("color is blue"); break;
    case "yellow" : console.log("color is yellow"); break;
    case "green" : console.log("color is green"); break;
    case "white" : console.log("color is white"); break;
    default : console.log("The color wont match"); break;
}
